void InsertSort(int *a,int size)
{
    int m,j,i,tmp;
    for (j = 1; j < size; ++j) {
        for (i = 0; i < j; ++i) {
           if (a[j]<a[i]) {
               m=j;
               tmp=a[m];
               while (m!=i) {
                   a[m]=a[m-1];
                   m--;
               }
               a[i]=tmp;
           } 
        }
    }
}
